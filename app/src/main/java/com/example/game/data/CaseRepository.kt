    // Picks a random case, ensuring never repeating until all cases are exhausted
    fun getUniqueCase(completedCaseTitles: Set<String>, playerCount: Int): Case {
        val available = cachedCases.filter { it.title !in completedCaseTitles }
        val pool = if (available.isNotEmpty()) available else cachedCases
        
        val randomCase = pool.random(Random(System.currentTimeMillis()))
        
        // Dynamically scale characters according to player count
        val charactersList = randomCase.characters.toMutableList()
        
        // Add more characters from the pool if the case has fewer than playerCount
        while (charactersList.size < playerCount) {
            val nextChar = suspectPool.firstOrNull { char ->
                char.name !in charactersList.map { it.name }
            } ?: suspectPool.random()
            
            charactersList.add(nextChar)
        }
        
        return randomCase.copy(characters = charactersList.take(playerCount))
    }
